package com.app.FunctionExact;

public class ArithmeticOperations {

	public static void main(String[] args) {

		System.out.println(Math.addExact(10, 20));
		System.out.println(Math.subtractExact(20, 40));
		System.out.println(Math.multiplyExact(25, 5));
		System.out.println(Math.floorDiv(25, 5));
		System.out.println(Math.floorMod(25, 2));
	}

}
